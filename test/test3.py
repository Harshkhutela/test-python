lines_needed = 2000
output_file = "challenge_2000_lines.py"

with open(output_file, "w") as f:
    for i in range(1, lines_needed + 1):
        # Introduce errors systematically
        if i % 3 == 0:
            f.write(f"def func{i}()\n")  # Missing colon
        elif i % 5 == 0:
            f.write(f"    print('Hello World {i}'\n")  # Missing parenthesis
        elif i % 7 == 0:
            f.write(f"x{i} = y{i} + 5\n")  # Undefined variable y{i}
        elif i % 11 == 0:
            f.write(f"if i = {i}:\n")  # Wrong assignment in if
        elif i % 13 == 0:
            f.write(f"for j in range({i})\n")  # Missing colon
        elif i % 17 == 0:
            f.write(f"pritn('Error line {i}')\n")  # Typo in print
        elif i % 19 == 0:
            f.write(f"return i*\n")  # Incomplete return
        elif i % 23 == 0:
            f.write(f"class MyClass{i}\n")  # Missing colon
        elif i % 29 == 0:
            f.write(f"while True\n")  # Missing colon
        elif i % 31 == 0:
            f.write(f"import nonexistmodule{i}\n")  # Invalid import
        elif i % 37 == 0:
            f.write(f"def recursive{i}():\n    return recursive{i}()\n")  # Infinite recursion
        else:
            f.write(f"val{i} = {i} + {i}\n")  # Valid line to mix in

print(f"{output_file} generated with {lines_needed}+ lines and 100+ intentional errors.")

